export const LINE_ADVANCE = 6;

// Precompute each glyph as 5 row-bitmasks (bit2=left .. bit0=right).
const TABLE: Record<string, number[]> = {};
for (const [ch, rows] of Object.entries(G)) {
  TABLE[ch] = rows.map((r) => ((r[0] === '#' ? 4 : 0) | (r[1] === '#' ? 2 : 0) | (r[2] === '#' ? 1 : 0)));
}

const BLANK = [0, 0, 0, 0, 0];

export function glyphRows(ch: string): number[] {
  const up = ch.toUpperCase();
  return TABLE[ch] || TABLE[up] || BLANK;
}
