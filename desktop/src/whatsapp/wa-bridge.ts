async function backfillFromSidecar() {
  try {
    const res = await fetch(`${WA_URL}/wa/chats`);
    if (!res.ok) return;
    const json = await res.json();
    const chats: Array<{ jid: string; name?: string }> = Array.isArray(json?.chats) ? json.chats : [];
    for (const c of chats) {
      if (!c?.jid) continue;
      await upsertChatMeta(c.jid, c.name || null);
      try {
        const hr = await fetch(`${WA_URL}/wa/history/${encodeURIComponent(c.jid)}?limit=${HISTORY_TOTAL}`);
        if (!hr.ok) continue;
        const hj = await hr.json();
        const messages: Array<{ id?: string; fromBot?: boolean; fromMe?: boolean; text: string; ts?: number }> =
          Array.isArray(hj?.messages) ? hj.messages : [];
        for (const m of messages) {
          if (!m || typeof m.text !== 'string' || !m.text.trim()) continue;
          const id = m.id || `bf_${c.jid}_${m.ts || 0}_${m.text.length}`;
          await recordMessage({
            id,
            jid: c.jid,
            fromBot: !!(m.fromBot ?? m.fromMe),
            text: m.text,
            ts: Number(m.ts) || Date.now(),
          }, c.name || null);
        }
        if (messages.length) logActivity('info', 'backfill', `[${c.jid.slice(-12)}] ${messages.length} msgs`);
      } catch {}
    }
  } catch {}
}

async function listContextFolderFiles(folder: string): Promise<string[]> {
  try {
    const entries = await readDir(folder);
    return entries
      .filter((e: any) => !e.isDirectory && typeof e.name === 'string' && e.name && !e.name.startsWith('.'))
      .map((e: any) => e.name as string)
      .sort();
  } catch {
    return [];
  }
}

function buildContextFolderBlock(files: string[]): string {
  if (!files.length) return '';
  const sample = files.slice(0, 40);
  return [
    '[AVAILABLE DOCUMENTS — files in the user-configured context folder for this chat]',
    ...sample.map(n => `- ${n}`),
    files.length > sample.length ? `(+${files.length - sample.length} more)` : '',
    'To send one of these documents to the user via WhatsApp, write `[SEND_DOC: <exact filename>]` on its own line in your reply.',
    'The bridge will strip the marker and send the file as a WhatsApp document attachment. Only filenames from the list above are valid; never invent paths.',
    '[END AVAILABLE DOCUMENTS]',
  ].filter(Boolean).join('\n');
}

const SEND_DOC_RE = /\[SEND_DOC:\s*([^\]\n]+?)\s*\]/g;

function extractSendDocDirectives(text: string, allowed: Set<string>): { cleaned: string; files: string[] } {
  const files: string[] = [];
  const cleaned = text.replace(SEND_DOC_RE, (_, raw: string) => {
    const name = String(raw).trim();
    if (!name || name.includes('/') || name.includes('\\') || name === '..' || name === '.') return '';
    if (!allowed.has(name)) return '';
    if (!files.includes(name)) files.push(name);
    return '';
  }).replace(/\n{3,}/g, '\n\n').trim();
  return { cleaned, files };
}

function joinPath(folder: string, name: string): string {
  const sep = folder.includes('\\') && !folder.includes('/') ? '\\' : '/';
  return folder.endsWith(sep) ? folder + name : folder + sep + name;
}

async function sendDocument(to: string, folder: string, filename: string): Promise<boolean> {
  const abs = joinPath(folder, filename);
  try {
    const res = await fetch(`${WA_URL}/wa/send-file`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ to, path: abs, kind: 'document', filename }),
    });
    if (!res.ok) {
      const detail = await res.text().catch(() => '');
      logActivity('error', 'send-doc', `→ ${to} ${filename}: HTTP ${res.status} ${detail}`.trim());
      return false;
    }
    logActivity('info', 'send-doc', `→ ${to} ${filename}`);
    const ts = Date.now();
    const id = `outdoc_${to}_${ts}_${Math.random().toString(36).slice(2, 10)}`;
    try { await recordMessage({ id, jid: to, fromBot: true, text: `[document: ${filename}]`, ts }); } catch {}
    return true;
  } catch (e) {
    logActivity('error', 'send-doc', `→ ${to} ${filename}: ${e instanceof Error ? e.message : String(e)}`);
    return false;
  }
}

function summarizeOlder(older: Array<{ role: string; content: string }>): string {
  if (!older.length) return '';
  const lines = older.map(m => {
    const who = m.role === 'assistant' ? 'Bot' : 'User';
    const text = m.content.replace(/\s+/g, ' ').trim();
    const trimmed = text.length > 200 ? text.slice(0, 199) + '…' : text;
    return `- ${who}: ${trimmed}`;
  });
  return [
    '[PAST CONVERSATION — already happened, do not re-answer, use only for context]',
    `Older exchanges with this chat (oldest first, ${older.length} message(s)):`,
    ...lines,
    '[END PAST CONVERSATION]',
  ].join('\n');
}

