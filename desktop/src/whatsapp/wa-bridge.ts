// EU AI Act Article 50(1) — disclosure that the interlocutor is an AI system.
// Appended to outbound replies sent to third-party contacts (not to the user's
// own owner chat where AI-origin is already obvious).
const AI_DISCLOSURE_SUFFIX = '\n\n— Réponse assistée par IA (MonkeyAgent). / AI-assisted reply.';

function withAiDisclosure(text: string): string {
  const t = (text || '').trim();
  if (!t) return t;
  // Idempotent — never double-append if the model already included the marker.
  if (/AI-assisted|assisté par IA|assistée par IA|generated by AI/i.test(t)) return t;
  return `${t}${AI_DISCLOSURE_SUFFIX}`;
}

