function parseYesNo(text: string): 'yes' | 'no' | null {
  const t = text.trim().toLowerCase();
  if (/^(yes|y|ok|oui|o|allow|approve|go|👍|✅)\b/.test(t)) return 'yes';
  if (/^(no|n|non|deny|refuse|stop|cancel|👎|❌)\b/.test(t)) return 'no';
  return null;
}

