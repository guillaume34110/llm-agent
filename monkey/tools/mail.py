def mail_label_add(message_id: str, label: str) -> str:
    """Add a label to a message without removing it from its current folder.
    On Gmail uses X-GM-LABELS (true labels). On other IMAP servers does an
    additive COPY to the target folder (message keeps INBOX presence)."""
    msg, account, err = _resolve_msg(message_id)
    if err:
        return err
    lbl = (label or "").strip()
    if not lbl:
        return "ERREUR: mail_label_add requires label"
    password = mail_store.get_password(account["id"])
    if not password:
        return f"ERREUR: no password in keychain for {account['email']}"
    sp = _special_folders(account, password)
    folder = msg.get("folder") or "INBOX"
    if sp.get("isGmail"):
        ok, ferr = imap_client.gmail_labels(
            account, password, int(msg["uid"]), [lbl], remove=False, folder=folder,
        )
    else:
        ok, ferr = imap_client.copy_to(
            account, password, int(msg["uid"]), lbl, folder=folder,
        )
    if not ok:
        return f"ERREUR: label add failed: {ferr}"
    return json.dumps({"ok": True, "id": msg["id"], "label": lbl, "gmail": bool(sp.get("isGmail"))}, ensure_ascii=False)


def mail_label_remove(message_id: str, label: str) -> str:
    """Remove a Gmail label from a message. On non-Gmail servers this is a
    no-op (labels are not first-class — use mail_move instead)."""
    msg, account, err = _resolve_msg(message_id)
    if err:
        return err
    lbl = (label or "").strip()
    if not lbl:
        return "ERREUR: mail_label_remove requires label"
    password = mail_store.get_password(account["id"])
    if not password:
        return f"ERREUR: no password in keychain for {account['email']}"
    sp = _special_folders(account, password)
    if not sp.get("isGmail"):
        return json.dumps({"ok": False, "id": msg["id"], "reason": "not_gmail_no_labels"}, ensure_ascii=False)
    ok, ferr = imap_client.gmail_labels(
        account, password, int(msg["uid"]), [lbl], remove=True,
        folder=msg.get("folder") or "INBOX",
    )
    if not ok:
        return f"ERREUR: label remove failed: {ferr}"
    return json.dumps({"ok": True, "id": msg["id"], "label": lbl, "removed": True}, ensure_ascii=False)


