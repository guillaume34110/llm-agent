def mail_reply(message_id: str, body: str, html: str = "",
               quote_original: bool = True, reply_all: bool = False) -> str:
    """Reply to a message. Auto-fills To/Subject/In-Reply-To/References from
    the original. Set reply_all=True to include Cc + other To recipients."""
    msg, account, err = _resolve_msg(message_id)
    if err:
        return err
    password = mail_store.get_password(account["id"])
    if not password:
        return f"ERREUR: no password in keychain for {account['email']}"

    sender = msg.get("from") or ""
    m = re.search(r"<([^>]+)>", sender)
    sender_addr = (m.group(1) if m else sender).strip()
    if not sender_addr:
        return "ERREUR: cannot determine reply address from original message"

    to_list = [sender_addr]
    cc_list: list[str] = []
    if reply_all:
        own = (account.get("email") or "").lower()
        for addr in msg.get("to") or []:
            a = str(addr).strip()
            if a and a.lower() != own and a.lower() != sender_addr.lower():
                to_list.append(a)
        for addr in msg.get("cc") or []:
            a = str(addr).strip()
            if a and a.lower() != own:
                cc_list.append(a)

    subj = msg.get("subject") or ""
    if not re.match(r"^\s*re\s*:", subj, re.I):
        subj = f"Re: {subj}".strip()

    orig_mid = (msg.get("messageId") or "").strip()
    in_reply_to = f"<{orig_mid}>" if orig_mid and not orig_mid.startswith("<") else orig_mid
    thread_root = (msg.get("threadId") or "").strip()
    root_fmt = f"<{thread_root}>" if thread_root and not thread_root.startswith("<") else thread_root
    refs_parts = [p for p in (root_fmt, in_reply_to) if p]
    seen_parts: list[str] = []
    for p in refs_parts:
        if p not in seen_parts:
            seen_parts.append(p)
    refs = " ".join(seen_parts)

    final_body = body or ""
    if quote_original:
        quoted = (msg.get("bodyText") or "")[:4000]
        quoted = "\n".join("> " + line for line in quoted.splitlines())
        final_body = f"{final_body}\n\nOn {time.strftime('%Y-%m-%d %H:%M', time.localtime((msg.get('dateTs') or 0)/1000))}, {sender} wrote:\n{quoted}"

    return mail_send(
        account["id"], to=to_list, subject=subj, body=final_body,
        cc=cc_list or None, in_reply_to=in_reply_to, references=refs, html=html or "",
    )


def mail_clean_inbox(account_id: str, mode: str = "safe",
                     dry_run: bool = True, batch_size: int = 25,
                     preview_limit: int = 100) -> str:
    """Triage INBOX using best-practice heuristics. Processes ONE BATCH per call.

    Workflow:
      1. First call with dry_run=true → returns the plan for the next
         `preview_limit` messages (no server-side change). Use to confirm.
      2. Then call repeatedly with dry_run=false → each call processes up to
         `batch_size` messages in a single IMAP session and returns a progress
         report. Keep calling while `done=false`.

    Actions per message:
      - keep    : flagged (\\Flagged) — stays in INBOX
      - archive : already read OR newsletter/notification OR stale unread
      - trash   : spam-like subject OR very old unread newsletter
      - review  : recent unread human-looking mail

    Modes:
      - safe       : applies archive/trash only; review stays in INBOX
      - aggressive : applies archive/trash AND archives review (inbox zero)

    Response includes `done` (true when no more actionable messages remain),
    `remainingInbox`, and `actionable` (msgs left that this mode WOULD process).
    """
    aid = (account_id or "").strip()
    if not aid:
        return "ERREUR: mail_clean_inbox requires account_id"
    account = mail_store.get_account(aid)
    if not account:
        return f"ERREUR: account not found: {aid}"
    password = mail_store.get_password(aid)
    if not password:
        return f"ERREUR: no password in keychain for {account['email']}"
    mode = (mode or "safe").lower()
    if mode not in ("safe", "aggressive"):
        return "ERREUR: mode must be 'safe' or 'aggressive'"
    batch_size = max(1, min(int(batch_size or 25), 100))
    preview_limit = max(1, min(int(preview_limit or 100), 500))

    now_ts = int(time.time() * 1000)
    sp = _special_folders(account, password)
    archive_folder = sp.get("archive") or sp.get("all") or "Archive"
    trash_folder = sp.get("trash") or "Trash"

    def classify_all(msgs: list[dict]) -> list[dict]:
        out: list[dict] = []
        for m in msgs:
            action, reason = _classify(m, now_ts=now_ts)
            if action == "review" and mode == "aggressive":
                action, reason = "archive", f"aggressive: {reason}"
            out.append({
                "id": m["id"], "uid": int(m["uid"]),
                "from": m.get("from", ""), "subject": m.get("subject", ""),
                "dateTs": int(m.get("dateTs") or 0),
                "action": action, "reason": reason,
                "dest": archive_folder if action == "archive" else (trash_folder if action == "trash" else ""),
            })
        return out

    # Server-side scan: pull UID+FLAGS+headers directly from IMAP. The local
    # SQLite cache only holds the newest 200 from the last mail_sync, which is
    # useless for inboxes with thousands of messages. Scan the newest portion
    # of the mailbox each call — once items get moved out, the next call sees
    # the next slice.
    def server_scan(limit: int) -> tuple[list[dict], int]:
        env = imap_client.fetch_envelopes(
            account, password, folder="INBOX", max_messages=limit, newest_first=True,
        )
        if not env.get("ok"):
            return ([], 0)
        msgs = []
        for e in env.get("messages") or []:
            msgs.append({
                "id": f"imap-{e['uid']}", "uid": int(e["uid"]),
                "from": e.get("from", ""), "subject": e.get("subject", ""),
                "dateTs": int(e.get("dateTs") or 0),
                "flags": e.get("flags") or [],
            })
        return (msgs, int(env.get("total") or 0))

    # ── DRY RUN — single shot, just preview the plan ────────────────────────
    if dry_run:
        msgs, inbox_total = server_scan(preview_limit)
        plan = classify_all(msgs)
        counts = {"keep": 0, "archive": 0, "trash": 0, "review": 0}
        for p in plan:
            counts[p["action"]] = counts.get(p["action"], 0) + 1
        return json.dumps({
            "ok": True, "dryRun": True, "mode": mode,
            "inboxTotal": inbox_total,
            "previewed": len(msgs),
            "counts": counts,
            "archiveFolder": archive_folder, "trashFolder": trash_folder,
            "plan": plan,
            "hint": "Call again with dry_run=false to apply. Loop until done=true.",
        }, ensure_ascii=False)

    # ── APPLY — process one batch in a single IMAP session ──────────────────
    # Walk newest INBOX UIDs until we collect `batch_size` actionable msgs.
    scan_limit = batch_size * 4
    scanned, inbox_total_initial = server_scan(scan_limit)
    scan_plan = classify_all(scanned)

    moves: list[dict] = []
    in_batch: list[dict] = []
    counts = {"keep": 0, "archive": 0, "trash": 0, "review": 0, "skipped": 0}
    for p in scan_plan:
        if p["action"] in ("keep", "review"):
            counts[p["action"]] += 1
            continue
        if len(moves) >= batch_size:
            break
        moves.append({"uid": p["uid"], "dest": p["dest"]})
        in_batch.append(p)
        counts[p["action"]] += 1

    if not moves:
        # Nothing to do in the scanned window. If scan covered the whole inbox
        # (scanned >= total) we're truly done; otherwise widen the scan.
        if len(scanned) >= inbox_total_initial:
            done_flag = True
            actionable_remaining = 0
        else:
            wider, _ = server_scan(min(inbox_total_initial, 1000))
            actionable_remaining = sum(
                1 for p in classify_all(wider) if p["action"] not in ("keep", "review")
            )
            done_flag = actionable_remaining == 0
        return json.dumps({
            "ok": True, "dryRun": False, "mode": mode,
            "batch": {"size": 0, "archived": 0, "trashed": 0, "errors": []},
            "scanned": len(scanned),
            "counts": counts,
            "remainingInbox": inbox_total_initial,
            "actionable": actionable_remaining,
            "done": done_flag,
            "elapsedMs": 0,
        }, ensure_ascii=False)

    res = imap_client.move_many(account, password, moves, folder="INBOX")
    # Drop applied msgs from local cache so we don't re-process them. Best
    # effort: rows stay in the DB until the next mail_sync, but updating the
    # folder field shifts them out of INBOX queries.
    errored_uids = {int(e.get("uid") or 0) for e in res.get("errors", [])}
    archived = 0
    trashed = 0
    for p in in_batch:
        if int(p["uid"]) in errored_uids:
            continue
        try:
            mail_store.update_folder(p["id"], p["dest"])
        except AttributeError:
            pass
        if p["action"] == "archive":
            archived += 1
        else:
            trashed += 1

    # New total = old total minus successful moves.
    moved_ok = archived + trashed
    remaining_inbox = max(0, inbox_total_initial - moved_ok)
    # Heuristic: if we filled a full batch, there's likely more actionable.
    actionable_remaining = remaining_inbox if len(moves) >= batch_size else 0

    return json.dumps({
        "ok": True, "dryRun": False, "mode": mode,
        "batch": {
            "size": len(moves),
            "archived": archived,
            "trashed": trashed,
            "errors": res.get("errors", []),
        },
        "scanned": len(scanned),
        "counts": counts,
        "archiveFolder": archive_folder, "trashFolder": trash_folder,
        "remainingInbox": remaining_inbox,
        "actionable": actionable_remaining,
        "done": remaining_inbox == 0 or (len(moves) < batch_size and actionable_remaining == 0),
        "elapsedMs": int(res.get("elapsedMs") or 0),
    }, ensure_ascii=False)


def mail_summarize_inbox(account_id: str = "", limit: int = 20) -> str:
    """Return a compact INBOX summary. Queries IMAP directly when possible so
    the totals match what Gmail/server actually shows (not just the local cache)."""
    aid = (account_id or "").strip() or None
    limit = max(1, min(int(limit or 20), 50))
    if aid:
        account = mail_store.get_account(aid)
        password = mail_store.get_password(aid) if account else ""
        if account and password:
            env = imap_client.fetch_envelopes(
                account, password, folder="INBOX", max_messages=limit, newest_first=True,
            )
            if env.get("ok"):
                items = []
                for e in env.get("messages") or []:
                    items.append({
                        "id": f"imap-{aid}-{e['uid']}",
                        "uid": int(e["uid"]),
                        "from": e.get("from", ""),
                        "subject": e.get("subject", ""),
                        "dateTs": int(e.get("dateTs") or 0),
                        "flags": e.get("flags") or [],
                    })
                unread = sum(1 for m in items if "\\Seen" not in (m.get("flags") or []))
                return json.dumps({
                    "accountId": aid,
                    "inboxTotal": int(env.get("total") or 0),
                    "shown": len(items),
                    "unreadInShown": unread,
                    "source": "imap",
                    "items": items,
                }, ensure_ascii=False)
    msgs = mail_store.list_messages(account_id=aid, folder="INBOX", limit=limit, offset=0)
    if not msgs:
        return "OK: inbox empty (local cache; provide account_id for live IMAP)"
    unread = sum(1 for m in msgs if "\\Seen" not in (m.get("flags") or []))
    return json.dumps({
        "accountId": aid,
        "total": len(msgs),
        "unread": unread,
        "source": "local",
        "items": [_slim_message(m) for m in msgs],
    }, ensure_ascii=False)
